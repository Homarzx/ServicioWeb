package com.example.app;

public class Usuario {

    public String edad;
    public String nombre;
    public String correo;
    public String user;
    public String pass;

    public Usuario(String edad, String nombre, String correo,String user, String pass) {
        this.edad = edad;
        this.nombre = nombre;
        this.correo = correo;
        this.user = user;
        this.pass = pass;
    }
}
